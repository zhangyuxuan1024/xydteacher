package net.iclassmate.teacherspace.bean.notice;

import java.util.List;

/**
 *
 * Created by xyd on 2016/3/18.
 */
public class Notice_Info {
    private List<Notice_exam_info> notice_exam_infoList;
    private int totalCount;
    private int unReadCount;

    public Notice_Info() {
        super();
    }

    public Notice_Info(List<Notice_exam_info> notice_exam_infoList, int totalCount, int unReadCount) {
        this.notice_exam_infoList = notice_exam_infoList;
        this.totalCount = totalCount;
        this.unReadCount = unReadCount;
    }

    public List<Notice_exam_info> getNotice_exam_infoList() {
        return notice_exam_infoList;
    }

    public void setNotice_exam_infoList(List<Notice_exam_info> notice_exam_infoList) {
        this.notice_exam_infoList = notice_exam_infoList;
    }


    public int getTotalCount() {
        return totalCount;
    }

    public void setTotalCount(int totalCount) {
        this.totalCount = totalCount;
    }

    public int getUnReadCount() {
        return unReadCount;
    }

    public void setUnReadCount(int unReadCount) {
        this.unReadCount = unReadCount;
    }
}
