package net.iclassmate.teacherspace.bean.general;

import java.util.List;

/**
 * Created by xydbj on 2016.2.26.
 */
public class SummaryInfo {
    private List<SubjectSummaries> subjectSummarieslist;
    private List<SummaryInfoDetails> summaryInfoDetailslist;

    public List<SubjectSummaries> getSubjectSummarieslist() {
        return subjectSummarieslist;
    }

    public void setSubjectSummarieslist(List<SubjectSummaries> subjectSummarieslist) {
        this.subjectSummarieslist = subjectSummarieslist;
    }

    public List<SummaryInfoDetails> getSummaryInfoDetailslist() {
        return summaryInfoDetailslist;
    }

    public void setSummaryInfoDetailslist(List<SummaryInfoDetails> summaryInfoDetailslist) {
        this.summaryInfoDetailslist = summaryInfoDetailslist;
    }

    @Override
    public String toString() {
        return "SummaryInfo{" +
                "subjectSummarieslist=" + subjectSummarieslist +
                ", summaryInfoDetailslist=" + summaryInfoDetailslist +
                '}';
    }
}
