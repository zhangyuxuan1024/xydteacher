package net.iclassmate.teacherspace.bean.general;

import java.util.List;

/**
 * Created by xydbj on 2016.2.26.
 */
public class SubjectSummaries {
    private String name;
    private List<SingleExamInfos> singleExamInfoslist;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public List<SingleExamInfos> getSingleExamInfoslist() {
        return singleExamInfoslist;
    }

    public void setSingleExamInfoslist(List<SingleExamInfos> singleExamInfoslist) {
        this.singleExamInfoslist = singleExamInfoslist;
    }

    @Override
    public String toString() {
        return "SubjectSummaries{" +
                "name='" + name + '\'' +
                ", singleExamInfoslist=" + singleExamInfoslist +
                '}';
    }
}
